<h1> Bienvenido a Mel&oacute;manos </h1>
<p> Antes de entrar... Resulta muy útil leerse lo siguiente:

 <br/>Melómano es la persona fanática de la música. Se trata de un individuo que siente un amor apasionado y excesivo por lo musical, dedicando tiempo y dinero al disfrute de este arte.


El término melómano fue creado por el dramaturgo y músico francés Pierre-Augustin Caron de Beaumarchais, quien en 1781 declara que la palabra proviene de un vocablo griego compuesto por el prefijo melos (“canto”) y el sufijo manos (de donde deriva la noción de manía). La melomanía, por lo tanto, es una especie de locura  o tendencia al furor vinculada a la música. <br />

Pero no fue hasta el siglo XX cuando un filósofo británico, Ludwig Wittgenstein, fue considerado como el primer melómano del que se tenga registro, según publica el sitio serialismo, ya que tenía una obsesión con un compositor y pianista alemán, Félix Mendelssohn, que quedó demostrado en su libro “Cultura y Valor”, donde no dejaba de alabar las obras del músico.
<br />
A diferencia de otras manías, la melomanía no es entendida como un trastorno psicológico o una enfermedad. Puede decirse que la melomanía no implica un riesgo para la persona ni para su entorno.

